
public class Word {

	private String langFrom;
	
	private String langTo;
	
	private String wordFrom;
	
	private String wordTo;
	
	private String exampleFrom;
	
	private String exampleTo;
	
	public void setLangFrom(String from) {
		
		langFrom = from;
	}
	
	public void setLangTo(String to) {
		
		langTo = to;
	}
	
	public void setWordFrom(String from) {
		
		wordFrom = from;
	}
	
	public void setWordTo(String to) {
		
		wordTo = to;
	}
	
	public void setExampleFrom(String from) {
		
		exampleFrom = from;
	}
	
	public void setExampleTo(String to) {
		
		exampleTo = to;
	}
	
	public String getLangFrom() {
		
		return langFrom;
	}
	
	public String getLangTo() {
		
		return langTo;
	}
	
	public String getWordFrom() {
		
		return wordFrom;
	}
	
	public String getWordTo() {
		
		return wordTo;
	}
	
	public String getExampleFrom() {
		
		return exampleFrom;
	}
	
	public String getExampleTo() {
		
		return exampleTo;
	}
}
